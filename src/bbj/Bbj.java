package bbj;

public class Bbj {

	private int bbjID;
	private String bbjTitle;
	private String userID;
	private String bbjDate;
	private String bbjContent;
	private int bbjAvailable;
	public int getBbjID() {
		return bbjID;
	}
	public void setBbjID(int bbjID) {
		this.bbjID = bbjID;
	}
	public String getBbjTitle() {
		return bbjTitle;
	}
	public void setBbjTitle(String bbjTitle) {
		this.bbjTitle = bbjTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getBbjDate() {
		return bbjDate;
	}
	public void setBbjDate(String bbjDate) {
		this.bbjDate = bbjDate;
	}
	public String getBbjContent() {
		return bbjContent;
	}
	public void setBbjContent(String bbjContent) {
		this.bbjContent = bbjContent;
	}
	public int getBbjAvailable() {
		return bbjAvailable;
	}
	public void setBbjAvailable(int bbjAvailable) {
		this.bbjAvailable = bbjAvailable;
	}
}
	